bool SieveOfEratosthenes(ull n)
{
    bool prime[(ull)sqrt(n) + 1];
    memset(prime, true, sizeof(prime));
    for (ull p = 2; p * p <= n; p++) {
        if (prime[p] == true) {
            for (ull i = p * p; i <= n; i += p)
                prime[i] = false;
        }
    }
    return prime[(ull)sqrt(n)];
}
bool isPerfectSquare(long double x)
{
    if (x >= 0) {
        long long sr = sqrt(x);
        return (sr * sr == x);
    }
    return false;
}
bool isPrime(int n)
{
    // Corner case
    if (n <= 1)
        return false;
 
    // Check from 2 to square root of n
    for (int i = 2; i <= sqrt(n); i++)
        if (n % i == 0)
            return false;
 
    return true;
}
